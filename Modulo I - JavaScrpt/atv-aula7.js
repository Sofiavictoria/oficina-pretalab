/* For loop 

for ([inicialização]; [condição]; [expressão final]) {
    declaração;
 }
 */

 //Exemplo 
 for (let i = 0; i < 9; i++) {
    console.log(i);
 }

/*Pratica
inicie a variável i recebendo o valor 0. Enquanto for menor que 11, 
acrescente mais um número a variável, então imprima na tela (2*i). */

for (let i = 0; i < 11; i++){
    console.log(2*i);
}